export function getTypeFromScores(
  anxietyPercent: number,
  avoidancePercent: number,
): AttachmentTypeCode {
  const highAnxiety = anxietyPercent >= 50;
  const highAvoidance = avoidancePercent >= 50;
  if (!highAnxiety && !highAvoidance) return "secure";
  if (highAnxiety && !highAvoidance) return "anxious";
  if (!highAnxiety && highAvoidance) return "avoidant";
  return "disorganized";
}

